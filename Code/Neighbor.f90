! Initial X- neighbor    
    function InitialNeighborX1(c)
        implicit none
        type(OctCell),pointer :: InitialNeighborX1, c

        if (c%nBGCell(1)==1) then
            InitialNeighborX1=>null()     
        else
            InitialNeighborX1=>Cell(c%nBGCell(1)-1,c%nBGCell(2),c%nBGCell(3))
        endif
        return    
    end function InitialNeighborX1

! Initial X+ neighbor     
    function InitialNeighborX2(c)
        implicit none
        type(OctCell),pointer :: InitialNeighborX2, c
        
        if (c%nBGCell(1)==nCell(1)) then
            InitialNeighborX2=>null()     
        else
            InitialNeighborX2=>Cell(c%nBGCell(1)+1,c%nBGCell(2),c%nBGCell(3))
        endif
        return    
    end function InitialNeighborX2
    
