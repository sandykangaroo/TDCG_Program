        Neighbor=>null()
        
        if(.not.associated(c%father))then
            Neighbor=>InitialNeighborX1(c)
            return
        endif
        cf=>c%father
        
        cfn=>NeighborX1(cf)
        if(associated(cfn))then
            if(c%fSplitType==0)then
                if(c%Location==1)then
                    if(.not.associated(cfn%son1))then
                        Neighbor=>cfn
