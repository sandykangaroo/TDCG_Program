            elseif(c%fSplitType==5)then
                if(c%Location==1)then
                    if(.not.associated(cfn%son1))then
                        Neighbor=>cfn
                    elseif(associated(cfn%son1))then
                        if(cfn%son1%fSplitType==1.or.cfn%son1%fSplitType==5)then
                            Neighbor=>cfn%son2
                        elseif(cfn%son1%fSplitType==3)then
                            Neighbor=>cfn%son1
