                    if(.not.associated(cfn%son1))then
                        Neighbor=>cfn
                    elseif(associated(cfn%son1))then
                        if(cfn%son1%fSplitType==0.or.cfn%son1%fSplitType==4)then
                            Neighbor=>cfn%son3
