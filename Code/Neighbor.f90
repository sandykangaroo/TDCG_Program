                            Neighbor=>cfn
                        endif
                    endif
                elseif(c%Location==2)then
                    Neighbor=>cf%son1
                elseif(c%Location==3)then
                    Neighbor=>cf%son4
                elseif(c%Location==4)then
                    if(.not.associated(cfn%son1))then
                        Neighbor=>cfn
                    elseif(associated(cfn%son1))then
                        if(cfn%son1%fSplitType==1.or.cfn%son1%fSplitType==3)then
